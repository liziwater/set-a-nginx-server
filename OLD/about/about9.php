document.addEventListener('DOMContentLoaded', function() {
    // 初始化 AOS 動畫
    AOS.init({
        duration: 800,
        easing: 'ease-out-cubic',
        once: true,
        offset: 100
    });
    
    // 滾動進度條
    const progressBar = document.querySelector('.scroll-progress-bar');
    
    window.addEventListener('scroll', function() {
        const windowHeight = window.innerHeight;
        const documentHeight = document.documentElement.scrollHeight;
        const scrollTop = window.pageYOffset || document.documentElement.scrollTop;
        const scrollPercent = (scrollTop / (documentHeight - windowHeight)) * 100;
        
        progressBar.style.width = scrollPercent + '%';
    });
    
    // 數字計數動畫 (如果您的 HTML 中有 data-count 屬性，此代碼才會生效)
    // 由於您的 HTML 中未包含 data-count，此處保持原樣，但不會有實際動畫效果
    const statObserver = new IntersectionObserver(function(entries) {
        entries.forEach(entry => {
            if (entry.isIntersecting && !entry.target.classList.contains('counted')) {
                entry.target.classList.add('counted');
                // animateNumber(entry.target); // 這裡需要 data-count 才能運作
            }
        });
    }, { threshold: 0.5 });
    
    document.querySelectorAll('.founder-stat-item .stat-value').forEach(stat => {
        statObserver.observe(stat);
    });
    
    // 領導者卡片點擊展開簡介
    document.querySelectorAll('.view-bio-btn').forEach(btn => {
        btn.addEventListener('click', function(e) {
            e.preventDefault();
            const card = this.closest('.leader-card');
            
            // 關閉其他已打開的卡片
            document.querySelectorAll('.leader-card.active').forEach(activeCard => {
                if (activeCard !== card) {
                    activeCard.classList.remove('active');
                    // 恢復按鈕文字 (如果需要，您需要找到該卡片內部的按鈕)
                }
            });
            
            // 切換當前卡片
            card.classList.toggle('active');
            
            // 更新按鈕文字
            if (card.classList.contains('active')) {
                this.textContent = '收起簡介';
            } else {
                this.textContent = '查看簡介';
            }
            
            // 滾動到卡片位置
            if (card.classList.contains('active')) {
                setTimeout(() => {
                    card.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
                }, 300);
            }
        });
    });
    
    // 卡片懸浮時的細微旋轉效果 (Parallax Tilt)
    document.querySelectorAll('.leader-card').forEach(card => {
        card.addEventListener('mousemove', function(e) {
            if (window.innerWidth > 768) {
                const rect = card.getBoundingClientRect();
                const x = e.clientX - rect.left;
                const y = e.clientY - rect.top;
                
                const centerX = rect.width / 2;
                const centerY = rect.height / 2;
                
                const rotateX = (y - centerY) / 30;
                const rotateY = (centerX - x) / 30;
                
                // 減少 scale 避免過度放大
                card.style.transform = `perspective(1000px) rotateX(${rotateX}deg) rotateY(${rotateY}deg) scale(1.02)`; 
            }
        });
        
        card.addEventListener('mouseleave', function() {
            card.style.transform = '';
        });
    });
});
</script>

